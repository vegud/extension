<template>
  <PageLayout :content-transition-name="contentTransitionName">
    <template #toolbar>
      <a class="self-center pr-4" @click="toSettings()">
        <fa icon="cog" class="h-icon hover:text-on-primary-hover-500"></fa>
      </a>
    </template>
    <template #content>
      <div class="flex flex-wrap h-full home-content--container" :class="{ 'bg-surface-100': current === 'select-card' }">
        <ResultFromSelect v-if="current === 'select-card'" class="m-2">
          <template #settings>
            <Settings>
              <template #info>
                <div>nothing yet</div>
              </template>
            </Settings>
          </template>
        </ResultFromSelect>

        <PageVideos v-else-if="current === 'page-videos'" class="w-full" />

        <div class="text-xs w-full flex flex-wrap items-center self-end justify-center mb-1 gap-1">
          <span>
            <span class="pr-1">Icons by</span>
            <a href="https://fontawesome.com/" target="_blank" class="inline-flex gap-0.5 text-primary-500 hover:text-primary-700 hover:underline">
              <span>Font Awesome</span>
              <fa icon="external-link-alt" class="self-center h-icon-sm pb-1" />
            </a>
          </span>
        </div>
      </div>
    </template>
  </PageLayout>
</template>

<script lang="ts">
import { computed, defineComponent, PropType } from 'vue';

import PageLayout from '@/components/PageLayout.vue';
import ResultFromSelect from '@/select/components/ResultFromSelect.vue';
import PageVideos from '@/video/components/PageVideos.vue';
import Settings from '@/subtitle/components/Settings.vue';
import { useInjectStore } from '@/composables/useInjectStore';

export default defineComponent({
  components: {
    PageLayout,
    ResultFromSelect,
    PageVideos,
    Settings,
  },
  props: {
    contentTransitionName: {
      type: String as PropType<string>,
      required: false,
      default: ''
    }
  },
  setup() {
    const appStore = useInjectStore('appStore');
    const navigationStore = useInjectStore('navigationStore');

    return {
      appState: appStore.state,
      toSettings: navigationStore.actions.toSettings,
      current: computed(() => {
        if (appStore.state.value.state !== 'NONE' && appStore.state.value.src === 'SEARCH') {
          return 'select-card';
        }
        if (appStore.state.value.state === 'NONE') {
          return 'page-videos';
        }
        return 'unknown';
      })
    };
  }
});
</script>

